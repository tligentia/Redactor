export const BOLD_TO_NORMAL: Record<string, string> = {};
for (const key in NORMAL_TO_BOLD) {
  BOLD_TO_NORMAL[NORMAL_TO_BOLD[key]] = key;
}

export const ITALIC_TO_NORMAL: Record<string, string> = {};
for (const key in NORMAL_TO_ITALIC) {
  ITALIC_TO_NORMAL[NORMAL_TO_ITALIC[key]] = key;
}

export const ALL_BOLD_CHARS = new Set(Object.values(NORMAL_TO_BOLD));
export const ALL_ITALIC_CHARS = new Set(Object.values(NORMAL_TO_ITALIC).filter(val => val in ITALIC_TO_NORMAL)); // Ensure only truly styled chars are included

export function convertToUnicodeStyled(text: string, style: 'bold' | 'italic'): string {
  const map = style === 'bold' ? NORMAL_TO_BOLD : NORMAL_TO_ITALIC;
  let result = '';
  for (const char of text) {
    result += map[char] || char;
  }
  return result;
}
